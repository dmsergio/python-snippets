# calc.py


class Calc:

    @staticmethod
    def add(x, y):
        """This method adds two numbers"""
        return x + y

    @staticmethod
    def subtract(x, y):
        """This method subtracts two numbers"""
        return x - y

    @staticmethod
    def multiply(x, y):
        """This method multiplies two numbers"""
        return x * y

    @staticmethod
    def divide(x, y):
        """This method divides two numbers"""
        return x / y
